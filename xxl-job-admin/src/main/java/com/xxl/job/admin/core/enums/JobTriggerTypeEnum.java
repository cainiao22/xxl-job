package com.xxl.job.admin.core.enums;

/**
 * @author yanpf
 * @date 2017-11-28 10:55:58
 * @description 触发类型，手动触发和自动触发，手动触发的如果发现父任务未执行会强制父任务执行
 *              自动触发的话，发现父任务未执行。会等待
 */
public enum JobTriggerTypeEnum {

    MANUAL_TRIGGER("手动触发"),
    AUTOMATIC_TRIGGER("自动触发");

    public final String desc;
    JobTriggerTypeEnum(String desc){
        this.desc = desc;
    }
}
