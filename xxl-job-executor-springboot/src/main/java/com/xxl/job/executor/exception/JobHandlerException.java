package com.xxl.job.executor.exception;

public class JobHandlerException  extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 4615797752762982381L;

	private int resCode;
	private String resMsg;
	
	
	
	public JobHandlerException(int resCode, String resMsg) {
		super();
		this.resCode = resCode;
		this.resMsg = resMsg;
	}
	public int getResCode() {
		return resCode;
	}
	public void setResCode(int resCode) {
		this.resCode = resCode;
	}
	public String getResMsg() {
		return resMsg;
	}
	public void setResMsg(String resMsg) {
		this.resMsg = resMsg;
	}
	@Override
	public String toString() {
		return "JobHandlerException [resCode=" + resCode + ", resMsg=" + resMsg + "]";
	}
	
	
}
